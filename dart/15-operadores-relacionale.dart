void main() {
  int a = 10;
  int b = 20;

  //Comprueba si dos valores son iguales
  print(a == b); //false
  //Comprueba si dos valores son diferentes
  print(a != b); //true
  //Comprueba si un valor es mayor que otro
  print(a > b);
  //Comprueba si un valor es menor que otro
  print(a < b);
  //Mayor o igual
  print(a >= b);
  //Menor o igual
  print(a <= b);
}
